<div>
  首先要感谢是牛客网内推的我，给我了这个机会，自我介绍下一个非常普通的211研究生（本科是一个普通的二本），之前也收到阿里内推offer，之后又投了蘑菇内推收到offer，今天来和大家分享下自己的面试经历.岗位：（应用软件开发岗）<br>
  蘑菇街的技术面是两面：一面先是自我介绍然后就问项目，项目中你遇到哪些问题，怎么解决的？在这个项目中你的贡献是什么？由于我的有一个项目中用到TCP，他让我介绍了TCP和UDP区别？最后又问了我平时有什么爱好？然后他问我有没有问题问他？大概二十分钟一面就结束了。<br>
  二面主要问项目，然后问道我简历上的C/C++，java，Python怎么学的？还做过其它东西吗？二面就十几分钟就结束了。<br>
  三面是Hr面主要问了去过杭州吗？阿里给我开了多少薪水？能去实习吗？等一些问题<br>
  从我经历过蘑菇街和阿里面试情况来看基础一定要扎实，一定要知其然知其所以然，一定要动手去做，可以坚持写博客。如果有锻炼机会一定抓住，比如比赛，比如项目，实习等。网易公开课，腾讯课堂，网易云课堂，传智播客等都有好多公开课你可以去学习。图书馆会有许多经典书籍你可以借来看，坚持就是胜利。<br> hr面试一定要诚实，不知道就说不知道。<br>
  <strong>特此说明：由于本人水平有限，难免哪里会有很多不足，望斧正。</strong>
  <br>
